                                <p style="text-align: center;"><span style="text-decoration: underline; color: #2880b9;"><span style="font-size: 20px;"><strong>ADMISSION PROCEDURE&nbsp;</strong></span></span></p>
                                <p>Online application form is required to be submitted through online portal, which will remain available on the IIT(ISM) website <em>iitism.ac.in </em>from March 15, 2021 to April 26, 2021. The GATE candidates will also have to register themselves at GATE COAP 2021. The admission of IIT Graduates and Sponsored candidates will be carried out through online portal of IIT (ISM) Dhanbad. After filling the form, the candidates are advised to take a print of the application and keep the same for record.</p>
                                <p>Candidates can apply for more than one programme and can select up to 10 preferences.&nbsp;</p>
                                <p>Non-refundable application fee for each programme for GATE candidates/IIT Graduates is Rs. 500/- for UR/EWS/OBC candidates and Rs. 250/- for SC/ST/Women candidates. The application fee needs to be paid through online payment gateway only along with the online application.</p>
                                <p>Non-refundable Application fee for each programme for all Sponsored candidates is Rs. 2000/-.</p>
                                <p>It is the <strong><em><u>responsibility of the candidate</u></em></strong>, to ensure that he/she satisfies all eligibility criteria for a particular programme, as mentioned in this information brochure, and submit complete and correct information in the Institute admission portal accordingly.</p>
                                <p>The Tech. admission will be through online counselling based on the GATE score and CGPA and Departmental Review (for IIT Graduates) as per schedule given in Table 5. The details of counselling procedure is given in Annexure-A.(Please refer to Information Brochure)</p>
                                <p>Image quality of the uploaded documents must be good enough to enable easy verification of the documents/items. Care should be taken to scan the documents (with file format jpg/jpeg) with the help of high quality scanner. The candidate should check the quality of the soft document before uploading.</p>
                                <p>The following documents are to be uploaded during the document uploading.</p>
                                <p>(i) Image files of your recent passport size photograph and signature.</p>
                                <p>(ii) Qualifying Degree Marks-Sheet of final semester / final year (7<sup>th</sup> semester / pre-final year marks-sheet for the &lsquo;appearing candidates&rsquo; whose final semester / final year results are pending).</p>
                                <p>(iii) Valid GATE score card as indicated in the application form (for GATE-candidates).</p>
                                <p>(iv) 2-year&rsquo;s experience certificate for candidates of Tech. in Mining Engineering/Opencast Mining (wherever applicable)</p>
                                <p>(v) Caste certificate/EWS income certificate to avail seat reservation under respective reserved category. (The OBC(NCL) certificate as per Annexure I and issued on or after <strong>April 1st , 2020</strong>).</p>
                                <p>(vi) Persons with Disability (PwD) are required to upload a certificate of disability from the <strong>authorized medical board</strong> attached to one of the following - Vocational Rehabilitation Centre (VRC) for Physically Handicapped persons/ Special Employment Exchange for Physically Handicapped/ Government Hospital (District and State level).</p>
                                <p>(vii) Endorsement certificate from the present employer (<em>for Sponsored candidates</em>).</p>
                                <p>(viii) Salary Bank Account statement/Employees' Provident Fund (EPF) statement(<em>for Sponsored candidates working in private firms/organizations</em>)</p>
                                <p>The admission of selected candidates through online counselling will be <strong>provisional</strong> after the payment of admission fee.</p>
                                <p>The candidate is to Report in-person at IIT(ISM), Dhanbad on the scheduled date, time and venue for completing his/her admission. If the candidate fails to report at the admission venue in-person along with the requisite documents in original by that date and time, he/she will not be considered afterwards for admission.</p>
