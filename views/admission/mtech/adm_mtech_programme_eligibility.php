            <li>Candidates must possess a Bachelor degree in Engineering/Technology or equivalent or a Master degree in Science/Arts/Commerce, from the institutes recognized by UGC/AICTE/Government. The qualifying degrees for each of the M. Tech. prorams are mentioned in Table 2.</li>
           
            
            <li>Candidates with equivalent degree and discipline of professional bodies (like Institution of Engineers (India), Institute of Chemical Engineers, Aeronautical Society of India, Institute of Electronics & Telecommunication Engineers, Indian Institute of Metals, etc.) are not eligible to apply in any M. Tech programs of this institute</li>
        
        <li>Candidates must have valid GATE score (remains valid at the time of Admission) in the appropriate GATE Paper as per Table 2. </li>
       
        <li>Candidates seeking admission to M. Tech. programs of the Institute should have 
            <ul style="margin-left:19px;">
                <li>Minimum 60% or CGPA/CPI of 6.0 in 10 point scale (without rounding off) in the qualifying degree for UR/OBC candidates.</li>
                <li>Minimum 55% or CGPA/CPI of 5.5 in 10 point scale (without rounding off) in the qualifying degree for SC/ST candidates.</li>
            </ul>
        </li>
        </ol>

        <p style="text-align: center;"><span style="font-size: 18px;"><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; <span style="color: #2880b9;">&nbsp;1.2	For IIT Graduates</span></strong><span style="color: #2880b9;"><strong>&nbsp;</strong></span></span></p>
    <p>Candidates graduating/ graduated from IITs with B.Tech. Degree having CGPA 8.00 and above (on a scale of 10) can apply without GATE Score. The respective department as per allocated seats will review such applications. The number of seats for such candidates will be supernumeric.</p>
    <p style="text-align: center;"><span style="font-size: 18px;"><strong>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; <span style="color: #2880b9;">&nbsp;1.3	For Sponsored Candidates </span></strong><span style="color: #2880b9;"><strong>&nbsp;</strong></span></span></p>
    <ul style="margin-left:19px;">
        <li>A candidate working in an academic institution/PSU/Govt. R&D orginizations/Govt. recognized private R&D orginizations/industry, is considered as a sponsored candidate. Such a candidate must be sponsored by his/her employer and must have been in employment with the sponsoring organization for at least 2 years at the time of admission. </li>
    <li>Candidates working in private firms/organizations have to submit their latest Salary Bank Account statement/Employees' Provident Fund (EPF) statement during uploading documents as well as during the admission process</li>
    <li>The applications of candidate sponsored by the organizations under a MoU between Institute and/or under QIP program shall be eligible for admisison</li>
    <li>The employer must undertake to relieve the candidate to stay in the campus for the specified period to enable the candidate to complete the program.</li>
    <li>Candidates must have valid GATE score (remains valid at the time of Admission) in the appropriate GATE Paper as per Table 2. </li>
    <li>Candidates seeking admission to M. Tech. PROGRAMS of the Institute should have 
        <ul style="margin-left:19px;">
            <li>Minimum 60% or CGPA/CPI of 6.0 in 10 point scale (without rounding off) in the qualifying degree for UR/OBC candidates.</li>
            <li>Minimum 55% or CGPA/CPI of 5.5 in 10 point scale (without rounding off) in the qualifying degree for SC/ST/PwD candidates.</li>
        </ul>
    </li>
    <li>For sponsored candidates having Bachelors degree from Centrally Funded Technical Institutes (CFTIs) having a CGPA of 8.0 or above, the requirement of GATE/NET is waived off.</li>
    <li>The number of seats for sponsored candidates will be supernumeric, subject to a maximum limit of 10% of total sanctioned seats.</li>

    </ul>
                                       
